#!/usr/bin/env node

/**
 * Sync script
 *
 * Copies source files from src/ into the WordPress theme directory (THEME_DIR).
 * In dev mode, also watches for changes and re-syncs.
 *
 * Usage:
 *   node bin/sync.js              # Initial sync only
 *   node bin/sync.js --watch      # Sync + watch for changes
 *   node bin/sync.js --production # Sync + copy vendor/ for deployment
 */

import { cpSync, mkdirSync, writeFileSync, existsSync, symlinkSync, lstatSync, readlinkSync, rmSync, unlinkSync } from 'fs';
import { resolve, dirname, relative, join } from 'path';
import { config } from 'dotenv';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = resolve(__dirname, '..');

config({ path: resolve(ROOT, '.env') });

const THEME_DIR = resolve(ROOT, process.env.THEME_DIR || './public/wp-content/themes/wp-twalp');
const isProduction = process.argv.includes('--production');
const isWatch = process.argv.includes('--watch');

console.log(`[sync] Theme directory: ${THEME_DIR}`);

/**
 * Ensure a directory exists.
 */
function ensureDir(dir) {
  mkdirSync(dir, { recursive: true });
}

/**
 * Copy a directory recursively.
 */
function copyDir(src, dest) {
  ensureDir(dest);
  cpSync(src, dest, { recursive: true, force: true });
}

/**
 * Create the autoload-path.php file that points to the repo root's vendor/.
 * Uses VENDOR_PATH env var if set (e.g. /var/www/vendor for Docker),
 * otherwise falls back to the local repo path.
 */
function writeAutoloadPath() {
  const vendorPath = process.env.VENDOR_PATH
    ? `${process.env.VENDOR_PATH}/autoload.php`
    : resolve(ROOT, 'vendor/autoload.php');
  const safeVendorPath = vendorPath.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
  const content = `<?php\n// Auto-generated by sync.js — do not edit\nreturn '${safeVendorPath}';\n`;
  writeFileSync(resolve(THEME_DIR, 'autoload-path.php'), content);
  console.log('[sync] Generated autoload-path.php');
}

/**
 * Create or verify the acf-json symlink.
 */
function setupAcfJsonSymlink() {
  const target = resolve(ROOT, 'src/acf-json');
  const link = resolve(THEME_DIR, 'acf-json');

  ensureDir(target);

  // Check if symlink already exists and points to the right place
  try {
    const stat = lstatSync(link);
    if (stat.isSymbolicLink()) {
      if (readlinkSync(link) === target) {
        console.log('[sync] acf-json symlink OK');
        return;
      }
      // Symlink exists but points elsewhere — remove it
      unlinkSync(link);
    } else {
      // It's a real directory (e.g. from copying the project) — backup then remove
      try {
        cpSync(link, target, { recursive: true });
      } catch {
        // Nothing to backup
      }
      rmSync(link, { recursive: true, force: true });
    }
  } catch {
    // Link doesn't exist, create it
  }

  symlinkSync(target, link, 'dir');
  console.log('[sync] Created acf-json symlink');
}

/**
 * Write the "hot" file for Vite dev server detection.
 */
function writeHotFile() {
  const distDir = resolve(THEME_DIR, 'dist');
  ensureDir(distDir);
  const devServer = process.env.VITE_DEV_SERVER || 'http://localhost:5173';
  writeFileSync(resolve(distDir, 'hot'), devServer);
  console.log('[sync] Created dist/hot file');
}

/**
 * Remove the "hot" file.
 */
function removeHotFile() {
  const hotFile = resolve(THEME_DIR, 'dist/hot');
  try {
    unlinkSync(hotFile);
  } catch {
    // File doesn't exist
  }
}

/**
 * Main sync operation.
 */
function sync() {
  ensureDir(THEME_DIR);

  // Copy PHP theme files
  copyDir(resolve(ROOT, 'src/theme'), THEME_DIR);
  console.log('[sync] Copied src/theme/ → theme');

  // Copy Twig templates
  copyDir(resolve(ROOT, 'src/views'), resolve(THEME_DIR, 'views'));
  console.log('[sync] Copied src/views/ → theme/views/');

  // Copy static assets
  if (existsSync(resolve(ROOT, 'src/fonts'))) {
    copyDir(resolve(ROOT, 'src/fonts'), resolve(THEME_DIR, 'assets/fonts'));
  }
  if (existsSync(resolve(ROOT, 'src/images'))) {
    copyDir(resolve(ROOT, 'src/images'), resolve(THEME_DIR, 'assets/images'));
  }

  // Generate autoload-path.php (points to repo root vendor/)
  if (!isProduction) {
    writeAutoloadPath();
  }

  // Setup ACF JSON symlink (dev only — in prod, files are copied)
  if (!isProduction) {
    setupAcfJsonSymlink();
    writeHotFile();
  } else {
    // In production, copy acf-json as real files (remove any existing symlink first)
    if (existsSync(resolve(ROOT, 'src/acf-json'))) {
      const acfDest = resolve(THEME_DIR, 'acf-json');
      try {
        const stat = lstatSync(acfDest);
        if (stat.isSymbolicLink()) {
          unlinkSync(acfDest);
        }
      } catch {
        // Doesn't exist yet, fine
      }
      copyDir(resolve(ROOT, 'src/acf-json'), acfDest);
    }
    // Copy vendor/ into theme for deployment
    if (existsSync(resolve(ROOT, 'vendor'))) {
      copyDir(resolve(ROOT, 'vendor'), resolve(THEME_DIR, 'vendor'));
      console.log('[sync] Copied vendor/ → theme/vendor/');
    }
    removeHotFile();
  }

  console.log('[sync] Done!');
}

/**
 * Watch mode: re-sync on file changes.
 */
async function watch() {
  const { watch: chokidarWatch } = await import('chokidar');

  const watcher = chokidarWatch([
    resolve(ROOT, 'src/theme'),
    resolve(ROOT, 'src/views'),
    resolve(ROOT, 'src/fonts'),
    resolve(ROOT, 'src/images'),
  ], {
    ignoreInitial: true,
    awaitWriteFinish: { stabilityThreshold: 100 },
  });

  watcher.on('all', (event, filePath) => {
    const rel = relative(resolve(ROOT, 'src'), filePath);
    console.log(`[sync] ${event}: ${rel}`);

    if (filePath.startsWith(resolve(ROOT, 'src/theme'))) {
      const dest = resolve(THEME_DIR, relative(resolve(ROOT, 'src/theme'), filePath));
      ensureDir(dirname(dest));
      try { cpSync(filePath, dest, { force: true }); } catch (err) { console.error(`[sync] Failed to copy ${rel}:`, err.message); }
    } else if (filePath.startsWith(resolve(ROOT, 'src/views'))) {
      const dest = resolve(THEME_DIR, 'views', relative(resolve(ROOT, 'src/views'), filePath));
      ensureDir(dirname(dest));
      try { cpSync(filePath, dest, { force: true }); } catch (err) { console.error(`[sync] Failed to copy ${rel}:`, err.message); }
    } else if (filePath.startsWith(resolve(ROOT, 'src/fonts'))) {
      const dest = resolve(THEME_DIR, 'assets/fonts', relative(resolve(ROOT, 'src/fonts'), filePath));
      ensureDir(dirname(dest));
      try { cpSync(filePath, dest, { force: true }); } catch (err) { console.error(`[sync] Failed to copy ${rel}:`, err.message); }
    } else if (filePath.startsWith(resolve(ROOT, 'src/images'))) {
      const dest = resolve(THEME_DIR, 'assets/images', relative(resolve(ROOT, 'src/images'), filePath));
      ensureDir(dirname(dest));
      try { cpSync(filePath, dest, { force: true }); } catch (err) { console.error(`[sync] Failed to copy ${rel}:`, err.message); }
    }
  });

  console.log('[sync] Watching for changes...');
}

// Run
sync();
if (isWatch) {
  await watch();
}
